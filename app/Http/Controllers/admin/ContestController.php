     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $validated = $request->validate([
            'fees' => 'required|numeric',
            'reward' => 'required|numeric',
            'title' => 'required|string',
        ]);

        // checking if already running a contest
        $security = Contest::where('status', 'open')->get();
        if ($security->count() > 0) {
            return redirect()->back()->withErrors("Already Open Contest Found, Please Mark it as Complete and try again. ");
        }

        // adding this new Contest
        $contest = Contest::create($validated);

        return redirect()->back()->with('success', 'New Contest Added Successfully');
    }

